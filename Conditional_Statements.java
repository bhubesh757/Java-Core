package com.company;

//Scanner package
import java.util.Scanner;

public class Main {


    public static void main(String[] args) {

        int num = 2;

//        Switch Statement
        switch (num) {
            case 2: {
                System.out.println("I am Two");
                break; //It breaks the statement;
            }
            case 4: {
                System.out.println("I am Three");
                break;
            }
            default: {
                System.out.println("All the cases are false");
                break;
            }
        }


    }

}
